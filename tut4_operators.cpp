#include <iostream>
using namespace std;

int main()
{
    //   Arithematic Operators:- Used to perfprm arithmetic operations
    int a = 5, b = 6;
    cout << "The following are the arithmetic operators: " << endl;
    cout << "Addition: a + b: " << a + b << endl;
    cout << "Subraction: a - b: " << a - b << endl;
    cout << "Multiplication: a * b: " << a * b << endl;
    cout << "Intiger Division: a / b: " << a / b << endl;
    cout << "Modular Division: a % b: " << a % b << endl;
    cout << "Pre Increment: ++a : " << ++a << endl; //First the value of a is increased and then printed
    cout << "Pre Decrement: --a : " << --a << endl;
    cout << "Post Increment: a++ : " << a++ << endl; // First a is printed and the value is increased so it prints increased value in next line/execution
    cout << "Post Decrement: a-- : " << a-- << endl;

    //    Assignment Operators:- Used to assign value to the variables
    int d = 45;
    float f = 4.44;
    char q = 'a';

    //    Comparison Operators:- Used to compare two values with each other
    cout << "The value of a == b is: " << (a == b) << endl;
    cout << "The value of a != b is: " << (a != b) << endl;
    cout << "The value of a <= b is: " << (a <= b) << endl;
    cout << "The value of a >= b is: " << (a >= b) << endl;
    cout << "The value of a > b is: " << (a > b) << endl;
    cout << "The value of a < b is: " << (a < b) << endl;

    //    Logical operators:- Used to perform logical operations
    /*
        && -> logical and operator
        || -> logical or perator
        ! -> logical not operator
        
    */
}